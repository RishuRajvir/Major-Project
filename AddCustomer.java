    private void proceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedActionPerformed
        try {
            // TODO add your handling code here:
            String customerid = CUST_ID.getText();
            String fname = firstname.getText();
            String lname = lastname.getText();
            String passport = pasport.getText();
            String nationid = nation.getText();
            String contactno = contact.getText();
            String gender = "";
            if(male.isSelected()){
                gender = "male";
            }else
                gender = "female";  
            
            DateFormat da  = new SimpleDateFormat("yyyy-MM-dd");
            String Date = da.format(dateofbirth.getDate());
            
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/singh_airlines","root","admin");
            
            pre  = con.prepareStatement("insert into customers(Customers_ID, First_Name, Last_Name, PassPortID, Nation_ID, Contact_No, Gender,DOB)values(?,?,?,?,?,?,?,?)");
            pre.setString(1,customerid );
            pre.setString(2,fname );
            pre.setString(3,lname );
            pre.setString(4,passport );
            pre.setString(5,nationid );
            pre.setString(6,contactno );
            pre.setString(7,gender );
            pre.setString(8,Date );
            
            pre.executeUpdate();    
            JOptionPane.showMessageDialog(null, "Customer Added Successfully!!");
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(false);
        AddCustomer cust = new AddCustomer();
        cust.setVisible(true);
    }//GEN-LAST:event_proceedActionPerformed

