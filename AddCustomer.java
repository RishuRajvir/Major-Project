    public void AutoID(){
        try {
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/singh_airlines","root","admin");
            pre=con.prepareStatement("Select MAX(Customers_ID) from singh_airlines.customers;");
            ResultSet rs = pre.executeQuery();
            rs.next();
            if(rs.getString("MAX(Customers_ID)")==null)
            {
                CUST_ID.setText("CS001");
            }else
            {
                long id = Long.parseLong(rs.getString("MAX(Customers_ID)").substring(2,rs.getString("MAX(Customers_ID)").length()));
                id++;
                CUST_ID.setText("CS"+String.format("%03d",id));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
